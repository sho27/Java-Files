public class IntegerArray {
    public static void main(String args[]) {
        //Declare an array
        int arr[] = new int[5];
        
        //assign elements to array
        arr[0] = 3;
        arr[1] = 7;
        arr[2] = 1;
        arr[3] = 9;
        arr[4] = 8;

        //Accessing the elements of the array
        for (int i = 0; i < 5; i++) {
            System.out.println(arr[i]);
        }
    }
}
